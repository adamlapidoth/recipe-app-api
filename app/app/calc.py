"""
Calculator functions
"""


def add(x: int, y: int) -> int:
    """
    Adds x and y and return results
    :param x: An integer
    :param y: An integer
    :return: The result
    """
    return x + y


def subtract(x: int, y: int) -> int:
    """
    Subtract x from y and return result.
    :param x: An integer
    :param y: An integer
    :return: The result
    """
    return y - x
